package king.pos;

public class position {
    public int posX, posY;
    public void setPosition(int x, int y)
    {
        this.posX = x;
        this.posY = y;
    }
    public int getPosX() {
        return this.posX;
    }
    public int getPosY() {
        return this.posY;
    }
}
